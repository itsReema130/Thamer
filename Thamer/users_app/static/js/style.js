function toggleSignup(){
	document.getElementById("login-toggle").style.backgroundColor="blue";
	 document.getElementById("login-toggle").style.color="blue";
	 document.getElementById("signup-toggle").style.backgroundColor="blue";
	 document.getElementById("signup-toggle").style.color="blue";
	 document.getElementById("login-form").style.display="none";
	 document.getElementById("signup-form").style.display="block";
 }
 
 function toggleLogin(){
	 document.getElementById("login-toggle").style.backgroundColor="blue";
	 document.getElementById("login-toggle").style.color="blue";
	 document.getElementById("signup-toggle").style.backgroundColor="blue";
	 document.getElementById("signup-toggle").style.color="blue";
	 document.getElementById("signup-form").style.display="none";
	 document.getElementById("login-form").style.display="block";
 }
 